#include "ft_printf.h"

int	put_str(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}

int	put_char(char c)
{
	write(1, &c, 1);
	return (1);
}
